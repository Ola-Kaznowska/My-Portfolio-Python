a = int(input("Podaj pierwszą liczbę: "))     
b = int(input("Podaj drógą liczbę: "))



dodawanie = a + b
odejmowanie = a - b
mnozenie = a * b
dzielenie = a / b



print(f"Wynik z dodawania to: {dodawanie} Wynik z odejmowania to: {odejmowanie} Wynik z mnożenia to: {mnozenie} Wynik z dzielenia to: {dzielenie}")